function renderRadar() {
  const subjects = scopedSubjects().filter((subject) => subject.normalizedAverage !== null);
  elements.chartEmpty.classList.toggle("hidden", subjects.length >= 3);
  const canvas = elements.chart;
  const context = canvas.getContext("2d");
  const ratio = window.devicePixelRatio || 1;
  const width = canvas.clientWidth;
  const height = canvas.clientHeight;
  canvas.width = width * ratio;
  canvas.height = height * ratio;
  context.setTransform(ratio, 0, 0, ratio, 0, 0);
  context.clearRect(0, 0, width, height);

  if (subjects.length < 3) {
    return;
  }

  const centerX = width / 2;
  const centerY = height / 2;
  const radius = Math.min(width, height) * 0.31;
  const sides = subjects.length;

  drawRadarGrid(context, centerX, centerY, radius, sides);
  drawRadarShape(context, centerX, centerY, radius, subjects);
  drawRadarLabels(context, centerX, centerY, radius, subjects);
}

function drawRadarGrid(context, centerX, centerY, radius, sides) {
  context.strokeStyle = "rgba(34, 34, 34, 0.1)";
  context.lineWidth = 1;
  for (let level = 1; level <= 4; level += 1) {
    const levelRadius = radius * (level / 4);
    context.beginPath();
    for (let index = 0; index < sides; index += 1) {
      const point = radarPoint(centerX, centerY, levelRadius, index, sides);
      index === 0 ? context.moveTo(point.x, point.y) : context.lineTo(point.x, point.y);
    }
    context.closePath();
    context.stroke();
  }

  for (let index = 0; index < sides; index += 1) {
    const point = radarPoint(centerX, centerY, radius, index, sides);
    context.beginPath();
    context.moveTo(centerX, centerY);
    context.lineTo(point.x, point.y);
    context.stroke();
  }
}

function drawRadarShape(context, centerX, centerY, radius, subjects) {
  context.beginPath();
  subjects.forEach((subject, index) => {
    const point = radarPoint(centerX, centerY, radius * (subject.normalizedAverage / 100), index, subjects.length);
    index === 0 ? context.moveTo(point.x, point.y) : context.lineTo(point.x, point.y);
  });
  context.closePath();
  context.fillStyle = "rgba(255, 56, 92, 0.16)";
  context.strokeStyle = "rgba(255, 56, 92, 0.95)";
  context.lineWidth = 2;
  context.fill();
  context.stroke();
}

function drawRadarLabels(context, centerX, centerY, radius, subjects) {
  const canvasWidth = context.canvas.clientWidth || context.canvas.width;
  context.font = "800 12px 'Instagram Sans', -apple-system, BlinkMacSystemFont, Segoe UI, sans-serif";
  context.textBaseline = "middle";

  subjects.forEach((subject, index) => {
    const point = radarPoint(centerX, centerY, radius + 48, index, subjects.length);
    const isLeft = point.x < centerX - 12;
    const isRight = point.x > centerX + 12;
    const maxWidth = isLeft
      ? Math.max(110, Math.min(240, point.x - 18))
      : isRight
        ? Math.max(110, Math.min(240, canvasWidth - point.x - 18))
        : 230;
    const lines = wrapCanvasText(context, titleCase(subject.name), maxWidth, 2);
    const percent = `${Math.round(subject.normalizedAverage)}%`;
    const lineHeight = 14;
    const totalHeight = (lines.length * lineHeight) + 16;
    const startY = point.y - (totalHeight / 2) + (lineHeight / 2);

    context.textAlign = isLeft ? "right" : isRight ? "left" : "center";
    context.fillStyle = "#222222";
    lines.forEach((line, lineIndex) => {
      context.fillText(line, point.x, startY + (lineIndex * lineHeight));
    });

    context.fillStyle = "#e31c5f";
    context.fillText(percent, point.x, startY + (lines.length * lineHeight) + 8);
  });
}

function wrapCanvasText(context, text, maxWidth, maxLines) {
  const words = String(text).split(/\s+/).filter(Boolean);
  const lines = [];
  let currentLine = "";

  words.forEach((word) => {
    const candidate = currentLine ? `${currentLine} ${word}` : word;
    if (context.measureText(candidate).width <= maxWidth || !currentLine) {
      currentLine = candidate;
      return;
    }
    lines.push(currentLine);
    currentLine = word;
  });

  if (currentLine) {
    lines.push(currentLine);
  }

  if (lines.length > maxLines) {
    const kept = lines.slice(0, maxLines);
    kept[maxLines - 1] = fitCanvasText(context, kept.slice(maxLines - 1).concat(lines.slice(maxLines)).join(" "), maxWidth);
    return kept;
  }

  return lines.map((line) => fitCanvasText(context, line, maxWidth));
}

function fitCanvasText(context, text, maxWidth) {
  if (context.measureText(text).width <= maxWidth) {
    return text;
  }

  const ellipsis = "...";
  let fitted = text;
  while (fitted.length > 1 && context.measureText(`${fitted}${ellipsis}`).width > maxWidth) {
    fitted = fitted.slice(0, -1);
  }
  return `${fitted.trim()}${ellipsis}`;
}

function radarPoint(centerX, centerY, radius, index, total) {
  const angle = (Math.PI * 2 * index) / total - Math.PI / 2;
  return {
    x: centerX + Math.cos(angle) * radius,
    y: centerY + Math.sin(angle) * radius,
  };
}

