async function refresh(data = null) {
  const payload = data || await api.getSubjects();
  state.payload = payload;
  state.academicYears = payload.academicYears || [];
  state.subjects = payload.subjects;

  if (!state.academicYears.some((year) => year.id === state.selectedAcademicYearId)) {
    const yearsWithSubjects = new Set(state.subjects.map((subject) => subject.academic_year_id));
    const defaultYear =
      state.academicYears.find((year) => year.mode === "school" && yearsWithSubjects.has(year.id))
      || state.academicYears.find((year) => yearsWithSubjects.has(year.id))
      || state.academicYears.find((year) => year.mode === "school")
      || state.academicYears[0]
      || null;
    state.selectedAcademicYearId = defaultYear?.id || null;
    if (state.selectedAcademicYearId) {
      localStorage.setItem("myregister-academic-year-id", String(state.selectedAcademicYearId));
    }
  }
  state.mode = selectedAcademicYear()?.mode || "school";

  const visibleSubjects = scopedSubjects();

  if (!visibleSubjects.some((subject) => subject.id === state.selectedSubjectId)) {
    state.selectedSubjectId = null;
    state.selectedGradeIds.clear();
    hideModal(elements.modal);
  }
  const selectedSubject = visibleSubjects.find((subject) => subject.id === state.selectedSubjectId);
  if (selectedSubject) {
    const visibleGradeIds = new Set(selectedSubject.grades.map((grade) => grade.id));
    state.selectedGradeIds = new Set([...state.selectedGradeIds].filter((id) => visibleGradeIds.has(id)));
  }
  if (!visibleSubjects.some((subject) => subject.id === state.gradeSubjectId)) {
    state.gradeSubjectId = visibleSubjects[0]?.id || null;
  }

  render(payload);
}

