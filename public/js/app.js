function scopedSummary(subjects) {
  let weightedTotal = 0;
  let weightTotal = 0;
  let totalGrades = 0;

  subjects.forEach((subject) => {
    subject.grades.forEach((grade) => {
      const weight = Math.max(Number(grade.weight), 0);
      weightedTotal += Number(grade.value) * weight;
      weightTotal += weight;
      totalGrades += 1;
    });
  });

  return {
    average: weightTotal ? weightedTotal / weightTotal : null,
    totalGrades,
  };
}

function weightDisplayLabel() {
  return t("weight").replace(/\s*%/g, "");
}

function currentLocale() {
  return {
    it: "it-IT",
    en: "en-US",
    es: "es-ES",
    fr: "fr-FR",
    de: "de-DE",
  }[state.language];
}

function formatDateTime(value) {
  if (!value) {
    return "--";
  }
  return new Intl.DateTimeFormat(currentLocale(), {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(new Date(value));
}

function formatDateOnly(value) {
  if (!value) {
    return null;
  }
  return new Intl.DateTimeFormat(currentLocale(), {
    dateStyle: "medium",
  }).format(new Date(`${value}T00:00:00`));
}

function scoreTone(value, scale) {
  const numericValue = Number(value);
  const numericScale = Number(scale);
  if (Number.isNaN(numericValue) || Number.isNaN(numericScale) || numericScale <= 0) {
    return "red";
  }

  if (Math.abs(numericValue - numericScale) < 0.001) {
    return "blue";
  }

  const ratio = numericValue / numericScale;
  if (ratio < 0.5) return "red";
  if (ratio < 0.6) return "yellow";
  if (ratio < 0.8) return "light-green";
  return "dark-green";
}

function gradeTone(value, scale) {
  return `grade-${scoreTone(value, scale)}`;
}

function textTone(value, scale) {
  return `tone-${scoreTone(value, scale)}`;
}

