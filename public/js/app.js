async function request(url, method = "GET", payload = null) {
  const options = {
    method,
    headers: payload ? { "Content-Type": "application/json" } : undefined,
    body: payload ? JSON.stringify(payload) : undefined,
  };
  const response = await fetch(url, options);
  if (!response.ok) {
    const message = await response.text();
    throw new Error(cleanServerMessage(message) || "Request failed");
  }
  return response.json();
}

function cleanServerMessage(message) {
  const match = message.match(/<p>Message: ([^<]+)<\/p>/);
  return match ? match[1] : message;
}

function t(key, replacements = {}) {
  const value = translations[state.language][key] || translations.it[key] || key;
  return Object.entries(replacements).reduce(
    (text, [name, replacement]) => text.replace(`{${name}}`, replacement),
    value,
  );
}

function titleCase(value) {
  return String(value)
    .trim()
    .split(/\s+/)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

function formatNumber(value) {
  return Number(value)
    .toFixed(2)
    .replace(/(\.\d*?)0+$/, "$1")
    .replace(/\.$/, "");
}

function formatAverage(value, scale = null) {
  if (value === null || Number.isNaN(Number(value))) {
    return "--";
  }
  return scale ? `${formatNumber(value)}/${formatNumber(scale)}` : formatNumber(value);
}

